package com.java.dao;

public class TestVar01 {
	public static void main (String[] args) {
		//变量的声明 （定义变量）（以年龄为案例）
		//java 是一个强类型的语言，只要声明变量就必须定义类型：在这里定义整数类型。
		int ages ; //定义一个整数类型的变量，变量名字为age
		//对变量进行操作：
		ages = 10;//变量名字为age,具体的值为10。
		System.out.print("年龄:");
		System.out.println(ages);
	}
}
